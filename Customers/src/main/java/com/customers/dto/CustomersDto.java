package com.customers.dto;

import java.util.Date;

public class CustomersDto {
	
	private String custName;
	private String transactionDate;
	private int monthlyTotal;
	private int monthlyReward;
	private int total;
	private int totalReward;
	
	
	public String getCustName() {
		return custName;
	}
	public void setCustName(String custName) {
		this.custName = custName;
	}
	public String getTransactionDate() {
		return transactionDate;
	}
	public void setTransactionDate(String transactionDate) {
		this.transactionDate = transactionDate;
	}
	public int getMonthlyTotal() {
		return monthlyTotal;
	}
	public void setMonthlyTotal(int monthlyTotal) {
		this.monthlyTotal = monthlyTotal;
	}
	public int getMonthlyReward() {
		return monthlyReward;
	}
	public void setMonthlyReward(int monthlyReward) {
		this.monthlyReward = monthlyReward;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getTotalReward() {
		return totalReward;
	}
	public void setTotalReward(int totalReward) {
		this.totalReward = totalReward;
	}
	
	
	
	
	
	
	

}
